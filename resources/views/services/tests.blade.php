            function onScriptsLoaded() {
                var $ = TelerWdMiniJS;
                var autoShow = window.telerWdOpts;
                var automaticWidgetShowTimer = null;
                var widgetShownToday = $.cookie("widgetShownToday");
                var triggerContent = document.getElementById("__telerWdTriggerContent");
                var loading = false;
                var showed = false;
                var tooltipTimer;
                var catchMode = 3;//1 - show on mouse out, 2 - show on timeout, 3 - both
                setUpAutoShowTimer();
                updateTooltipPosition({clientX: triggerContent.offsetLeft});
                $.bind(document.body, "click", function (event) {
                    var element = event.target;
                    var parents = [];
                    var maxDepth = 10;
                    while (element && element.getAttribute) {
                        if (element.getAttribute("href") == "#tlcallback") {
                            showCallbackWidget();
                            $.trackGa('CallCatcher', 'Show');
                            break
                        } else if (!element.className) {
                            maxDepth--;
                            if (maxDepth <= 0) {
                                break
                            }
                            element = element.parentNode;
                            continue
                        } else {
                            var newElementClass = ' ' + element.className + ' ';
                            if (newElementClass.indexOf(" tl-call-catcher ") !== -1) {
                                showCallbackWidget();
                                $.trackGa('CallCatcher', 'Show');
                                break
                            }
                        }
                        maxDepth--;
                        if (maxDepth <= 0) {
                            break
                        }
                        element = element.parentNode;
                    }
                });

                function setUpAutoShowTimer() {
                    autoShow.catchMode && (catchMode = autoShow.catchMode);
                    if (!autoShow.autoShowDisable && (!autoShow.autoShowOnce || (autoShow.autoShowOnce && !widgetShownToday)) && catchMode != 2) {
                        var isActive;
                        var left = autoShow.timeout;

                        $.bind(window, "mouseover", function () {
                            isActive = true;
                        });
                        $.bind(window, "mouseout", function (event) {
                            isActive = false;
                        });
                        left && (automaticWidgetShowTimer = setInterval(function () {
                            if (!isActive) {
                                return;
                            }
                            if (!left) {
                                clearInterval(automaticWidgetShowTimer);
                                automaticWidgetShowTimer = null;
                                if (left === 0 && !showed && (!$.cookie("widgetShownToday") || !autoShow.autoShowOnce)) {
                                    showCallbackWidget();
                                    $.trackGa('CallCatcher', 'Show proactive');
                                    window.__telerWdProactiveShow = true;
                                }
                                return;
                            }
                            left -= 1;
                        }, 1000));
                    }

                    setTimeout(function () {
                        $.removeClass(document.getElementById("__telerWdTriggerContent"), "teler-wd__trigger_hidden");
                    }, 10);
                    initCallbackButton();
                }

                function initCallbackButton() {
                    var $ = TelerWdMiniJS;
                    var trigger = document.getElementById('__telerWdTriggerContent');

                    $.draggable(trigger, function (wasMoved) {
                        if (trigger && wasMoved) {
                            showCallbackWidget();
                            $.trackGa('CallCatcher', 'Show');
                        } else {
                            updateTooltipPosition(this);
                        }
                    });

                    tooltipTimer = setTimeout(function () {
                        ($.width() > 520) && !$.cookie("telerWdTooltipClosed") && $.addClass(trigger, "teler-wd__tooltip_show");
                    }, 5000);
                    $.bind(trigger, "mouseenter", function () {
                        ($.width() > 520) && (!$.cookie("telerWdTooltipClosed")) && $.addClass(trigger, "teler-wd__tooltip_show");
                        $.removeClass($.select(".teler-wd__flipper"), "teler-wd__flipper_hover");
                    });
                    $.bind(trigger, "mouseleave", function () {
                        $.removeClass(trigger, "teler-wd__tooltip_show");
                        $.removeClass($.select(".teler-wd__flipper"), "teler-wd__flipper_hover");
                    });
                    $.bind(window, "mouseout", function (event) {
                        if (event.clientY < 0 && !showed && (!$.cookie("widgetShownToday") || !autoShow.autoShowOnce) && catchMode != 1 && !autoShow.autoShowDisable) {
                            showCallbackWidget();
                            window.__telerWdProactiveShow = true;
                            $.trackGa('CallCatcher', 'Show proactive');
                        }
                    });
                }

                function showCallbackWidget() {
                    if (loading) {
                        return;
                    }

                    loading = true;
                    showed = true;
                    var widget = $.select(".teler-wd");
                    var trigger = document.getElementById('__telerWdTriggerContent');
                    if (!widget) {
                        $.ajax("//" + telerWdDomain + "/rest/public/widget/call-catchers/" + telerWdWidgetId + "/content",
                            function (data, resp) {
                                loading = false;
                                if (resp.status != 200 || !data) {
                                    return
                                }
                                $.addClass(document.getElementById("__telerWdTriggerContent"), "teler-wd__trigger_hidden");
                                var telerWdOpts = JSON.parse(data);
                                $.toDom(telerWdOpts.content);
                                window.telerWdOpts.offHoursInfo = telerWdOpts.offHoursInfo;
                                window.telerWdOpts.preferCountries = telerWdOpts.preferCountries;
                                window.telerWdOpts.onlyPreferCountries = telerWdOpts.onlyPreferCountries;
                                if (automaticWidgetShowTimer != null) {
                                    clearTimeout(automaticWidgetShowTimer);
                                }
                                tooltipTimer && clearTimeout(tooltipTimer);
                                $.removeClass(document.getElementById("__telerWdTriggerContent"), "teler-wd__tooltip_show");
                                saveCookieWidgetShownToday();
                            });
                    } else {
                        $.removeClass(widget, "teler-wd_hidden");
                        $.addClass(trigger, "teler-wd__trigger_hidden");
                        loading = false;
                    }
                }

                function saveCookieWidgetShownToday() {
                    var currentDate = new Date();
